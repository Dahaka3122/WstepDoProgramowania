"""Grupa 1. Użytkownik podaje dwie liczby całkowite.
Wypisz na ekranie wynik podniesienia pierwszej liczby do potęgi, którą jest druga liczba."""

x = float(input("Podaj pierwszą liczbę całkowitą: "))
y = float(input("Podaj do której potęgi ma być podniesiona liczba: "))

print("x do potęgi y = ", x ** y)
